def _format_anchor_block(anchor: dict) -> str:
    """Format a single anchor as a calibration example block."""
    return f"""--- مثال معايرة ---
القصيدة: {anchor['title']} — {anchor['poet']}
({anchor['tier']})
مقطع:
{anchor['excerpt']}
الدرجة المرجعية: {anchor['target_score']}/100
---"""


def _build_anchor_section() -> str:
    """Build the full anchor calibration section for prompts."""
    ordered = [
        "imru_alqays", "kaab", "mutanabbi", "darwish",
        "ibn_zaydun", "abu_aswad_competent", "ibn_arabi",
        "abu_firas_minor", "flat_praise",
    ]
    blocks = [_format_anchor_block(ANCHORS[k]) for k in ordered]
    return "\n\n".join(blocks)


